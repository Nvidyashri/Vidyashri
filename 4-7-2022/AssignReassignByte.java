class AssignReassignByte{
public static void main(String[] args){
byte age;
byte place_value_of_alphabate;
byte weight_of_goat;
byte number_of_floors;
byte number_of_fingers;
age = 22;
place_value_of_alphabate= 12;
weight_of_goat= 9;
number_of_floors= 5;
number_of_fingers= 20;
System.out.println("\n");
System.out.println(age);
System.out.println(place_value_of_alphabate);
System.out.println(weight_of_goat);
System.out.println(number_of_floors);
System.out.println(number_of_fingers);
System.out.println("\n");
age= 50;
place_value_of_alphabate= 20;
weight_of_goat= 7;
number_of_floors= 10;
number_of_fingers= 25;
System.out.println("After reinitialization");
System.out.println("\n");
System.out.println(age);
System.out.println(place_value_of_alphabate);
System.out.println(weight_of_goat);
System.out.println(number_of_floors);
System.out.println(number_of_fingers);
}
}

